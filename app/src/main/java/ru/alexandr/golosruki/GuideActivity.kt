package ru.alexandr.golosruki

import android.os.Bundle
import android.widget.LinearLayout
import android.widget.ScrollView
import androidx.activity.ComponentActivity

class GuideActivity : ComponentActivity() {

    override fun onCreate(savedInstanceState: Bundle?) {
        super.onCreate(savedInstanceState)
        val col = UiKit.column(this)
        col.addView(UiKit.title(this, "Гайд по управлению"))
        col.addView(UiKit.subtitle(this, "Сначала скажите «Иван» — после этого команды идут подряд. Через 30 сек тишины — снова «Иван»."))

        section("🟢 Активация", listOf(
            "«Иван» — разбудить (всегда снимает паузу)",
            "«Иван привет» — разбудить и разблокировать экран",
            "«стоп» / «пауза» — приостановить",
            "«слушай» / «продолжи» — продолжить",
            "Через 30 сек тишины — сон (нужно снова «Иван»)"
        ), col)

        section("🧭 Навигация", listOf(
            "«назад» — кнопка назад",
            "«домой» — на главный экран",
            "«недавние» — список приложений",
            "«шторка» — панель уведомлений"
        ), col)

        section("👆 Нажатия по номерам", listOf(
            "«номера» — пронумеровать кнопки на экране",
            "«нажми 5» — нажать элемент №5",
            "«долгое нажатие 5» — удержание",
            "«двойное нажатие 5» — двойной тап",
            "«скрой» — убрать номера"
        ), col)

        section("🔢 Сетка (точное попадание)", listOf(
            "«сетка» — экран делится на 12 ячеек",
            "«нажми 7» — приблизиться к ячейке 7",
            "«нажми 3» — точное нажатие внутри",
            "Если в ячейке есть кнопка — палец «прилипает» к ней"
        ), col)

        section("📜 Прокрутка", listOf(
            "«вниз» / «вверх» — листать содержимое (умный скролл нужного списка)",
            "«влево» / «вправо» — горизонтальный свайп"
        ), col)

        section("📞 Контакты и приложения", listOf(
            "«позвони жене» — звонок (настраивается в Настройках)",
            "«открой телеграм» — запуск приложения"
        ), col)

        section("✍️ Ввод текста", listOf(
            "«напиши» или «диктовка» — режим свободного ввода",
            "Говорите текст — он печатается в активное поле",
            "«готово» — выйти из диктовки",
            "«удали» — стереть символ, «ввод» — Enter",
            "Совет: для имён/контактов точнее задать «быстрые номера» в Настройках, чем диктовать"
        ), col)

        section("🆘 Помощь", listOf(
            "«сос» / «спасите» / «тревога» — звонок + СМС с геолокацией на SOS-номер",
            "«помощь» — краткая подсказка на экране"
        ), col)

        section("⚠️ Если не реагирует", listOf(
            "Проверьте, что служба спец. возможностей включена",
            "Скажите «Иван» — могла включиться пауза или сон",
            "Шум мешает — нужен микрофон ближе",
            "Откройте «Логи» и посмотрите, что распозналось"
        ), col)

        setContentView(ScrollView(this).apply { addView(col) })
    }

    private fun section(header: String, items: List<String>, col: LinearLayout) {
        val card = UiKit.card(this)
        card.addView(UiKit.sectionHeader(this, header))
        for (i in items) card.addView(UiKit.body(this, "•  $i"))
        col.addView(card)
    }
}
