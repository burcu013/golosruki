object Vocabulary {

    private val base = listOf(
        // активация и разблокировка
        "иван", "привет",
        // навигация и управление
        "назад", "домой", "недавние", "шторка", "уведомления",
        "быстрые", "настройки", "заблокируй", "блокировка", "скриншот",
        "номера", "кнопки", "сетка", "ячейка",
        "нажми", "нажать", "выбери", "долгое", "двойное", "нажатие", "удержание",
        "удали", "стереть", "сотри", "выдели", "выделить", "очисти", "очистить", "всё",
        "смахни", "свайпни", "ввод", "отправь", "энтер",
        "перетащи", "потяни", "перенеси", "вставь", "вставить",
        "цифры", "цифрами", "конца", "самый", "начало", "наверх",
        "скрой", "убери", "спрячь", "отмена",
        "стоп", "хватит", "пауза", "слушай", "продолжи", "молчи", "замолчи", "замри",
        "помощь", "команды",
        // SOS
        "сос", "спасите", "тревога", "помогите",
        // звонок и звук
        "ответь", "ответить", "прими", "сбрось", "отклони", "заверши", "трубку", "отбой",
        "беззвучно", "заглуши", "тихо", "громче", "тише", "звук", "повтори",
        "воспроизведи", "играй", "плей", "останови", "выход",
        // персональные действия
        "позвони", "набери", "открой", "запусти",
        // диктовка и ввод
        "напиши", "введи", "диктовка", "печатать", "готово", "конец",
        // свайпы
        "вверх", "вниз", "влево", "вправо", "листай",
        // числа
        "ноль", "один", "одна", "два", "две", "три", "четыре", "пять",
        "шесть", "семь", "восемь", "девять", "десять", "одиннадцать", "двенадцать",
        "тринадцать", "четырнадцать", "пятнадцать", "шестнадцать", "семнадцать",
        "восемнадцать", "девятнадцать", "двадцать", "тридцать", "сорок", "пятьдесят"
    )

    /** Строит JSON-грамматику с учётом слова активации, персональных имён и приложений. */
    fun buildGrammar(personal: PersonalConfig, wakeWord: String): String {
        val words = base.toMutableSet()
        wakeWord.split(" ").forEach { if (it.isNotBlank()) words.add(it) }
        personal.contacts.keys.forEach { name -> name.split(" ").forEach { words.add(it) } }
        personal.apps.keys.forEach { name -> name.split(" ").forEach { words.add(it) } }
        personal.customApps.keys.forEach { phrase -> phrase.split(" ").forEach { words.add(it) } }
        val json = words.joinToString(",") { "\"$it\"" }
        // [unk] позволяет модели выдавать «неизвестно» вместо подбора ближайшего слова к шуму
        return "[$json,\"[unk]\"]"
    }
}
